// Declare variables and assign values of different data types

// String (name of the item)
let itemName = "Laptop";

// Number (price of the item)
let itemPrice = 999.99;

// Boolean (whether the item is in stock)
let inStock = true;

// Array (list of item features)
let features = ["16GB RAM", "512GB SSD", "Intel i7 Processor"];

// Object (details of the seller)
let sellerDetails = {
    name: "Tech Store",
    rating: 4.5
};

// Undefined (no value assigned yet)
let discountCode;

// Null (intentionally empty value)
let warranty = null;

// Print the values and types of the variables
console.log("Item Name:", itemName, "| Type:", typeof itemName);
console.log("Item Price:", itemPrice, "| Type:", typeof itemPrice);
console.log("In Stock:", inStock, "| Type:", typeof inStock);
console.log("Features:", features, "| Type:", typeof features);
console.log("Seller Details:", sellerDetails, "| Type:", typeof sellerDetails);
console.log("Discount Code:", discountCode, "| Type:", typeof discountCode);
console.log("Warranty:", warranty, "| Type:", typeof warranty);
